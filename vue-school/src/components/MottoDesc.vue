          3.笃行：语出《礼记·中庸》：“博学之，审问之，慎思之，明辨之，笃行之”；《礼记·儒行》：“儒有博学而不穷，笃行而不倦”。“笃”者，忠贞不渝、专心致志、持之以恒也；笃行即在学有所得的基础上践履所学，知行合一。恪守“笃行”校训，归宿点是使办学的初心及其成果走出“象牙塔”，实现教育报国的梦想，自觉成为“自由、平等、公正、法治”美好社会的践行者。
