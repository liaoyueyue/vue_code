        </p>
        <p>
          2.勤奋：出自清·平步青《霞外攟屑·掌故·林西厓方伯》：“似此勤奋出力之员，岂可拘其家世，不加奖励。”意思是认认真真，努力干好一件事情，不怕吃苦，踏实工作。勤奋治学、勤奋工作。学生要勤奋学习，教职员工要勤奋工作；幸福是奋斗出来的，只有勤奋才能出成绩、出成果，是保障学校高质量发展应有的精神状态。
        </p>

