          荣少勤学，讲论不怠，治《欧阳尚书》”。“学而时习之不亦说乎”，业精于勤而荒于嬉。人生就是一个不断学习、不断积累、不断超越自我的过程。勤学二字旨在提醒学生明确学习目标，勤于思考、善于学习、终身学习，在勤学中建立优势，在勤学中达成目标，在勤学中坚定志向，在勤学中奉献社会，在勤学中圆满人生，在勤学中实现梦想。
