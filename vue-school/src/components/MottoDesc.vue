        </p>
        <p>
          4.卓越：卓越是一种持续自我超越的境界，不懈追求卓越是对自己、对事业高度责任感的体现。学校坚持走“高端化、国际化、个性化”发展之路，以培养高素质应用型人才为目标，更好地为国家经济社会发展服务。每一位师生都要积极行动、持续创新、成为越来越优秀的自己，同时推动学校创一流的科研成果、培养一流的创新人才、培育一流的先进文化，成为学生满意、家长放心、社会尊重的大学，是学校高质量发展的目标。
        </p>
